<h3>開場：</h3>
<p>布里斯比羅吉、金德‧伯勒達伊。</p>
<h3>大地之母單位：</h3>
<p>無</p>
  
<h2>主要任務</h2>
<div class="hrn1"></div>
<div class="n1">
<p>布里斯比羅吉必須活著。</p>
<p>第一階段可選軍隊與任務（此3項任務獎勵只影響初始部隊）</p>
  <ul>
    <li>點擊諸葛弩手來選擇勁弩單位。</li>
    <li>帶上至少10個你的人馬衝到敵軍插著旗幟的山頭上。</li>
    →→勁弩單位將會得到+2攻擊
  </ul>
  <ul>
    <li>點擊聖騎士來選擇騎士單位。</li>
    <li>洗劫敵軍的馬車，破壞他們的大帳營。</li>
    →→騎士單位+4攻擊
  </ul>
  <ul>
    <li>點擊帝王駱駝騎兵以選擇大象單位。</li>
    <li>殺死15個敵軍單位！</li>
    →→大象單位+5攻擊
  </ul>
<ol>
  <li>打敗古爾大軍（綠）。</li>
  <li>擊敗默罕穆德•古爾（藍），攻佔巴廷達（黃）。</li>
</ol>
</div>
</div>

<h2>次要任務</h2>
<div class="hrn1"></div>
<div class="n1">
  <ul>
    <li>摧毀古爾大營裡的城堡，那是敵軍指揮和生產精銳單位的大本營。</li>
    <li>摧毀城池裡的清真寺，以便強化你們的信仰。</li>
    <li>派5個村民去要塞東邊的木材堆那。</li>
    <li>破壞巴廷達東邊部的補給營的市集。</li>
    <li>破壞西邊連接引水渠的蓄水庫。</li>
  </ul>

<h2>任務提示</h2>
<div class="hrn1"></div>
<div class="n1">
<ol>
  <li>你可以隨意搭配不同的兵種組合來指揮這場戰鬥，但是要隨機應變，弓箭手應站在大象後面遠端射擊，騎兵則善於兩翼包抄。</li>
  <li>要記得為你的軍隊獲得經驗獎勵，這可以幫你贏下第一場戰鬥。</li>
  <li>切斷敵軍的供給線可以削弱巴廷達的抵抗力量。</li>
  <li>古爾在北邊遠處的大本營易守難攻，如果能早點去攻打的話比晚點去好。</li>
  <li>別只派一種兵力去作戰，因為他們的忠誠度可能會受戰局影響。(玩家生產的象弓騎兵會在古爾夜襲時叛變)</li>
</ol>
</div>
</div>

<h2>斥侯回報</h2>
<div class="hrn1"></div>
<div class="n1">
<ul>
  <li>布里斯比羅吉‧兆汗（紅）的大軍在戰場南邊集結了，其中有中裝和輕步兵、步弓兵、駱駝騎兵、僧侶、騎士和大象。</li>
  <li>默罕穆德‧古爾的大軍（綠）佔據了戰場的西北角，他的單位有馬弓騎兵、輕騎兵、駱駝騎兵、馬木留克奴隸兵、騎兵和騎驢僧侶。此外，古爾的軍隊在北邊有座大本營。</li>
  <li>巴廷達（黃）是西北角上的一座防衛森嚴的城池，駐紮軍隊有馬弓騎兵、重裝長槍兵和戰矛兵。</li>
  <li>塔蘭村（水藍）是東南邊的一座小村莊，它們是中立的，但是我們可以利用它來和兆汗人做貿易。</li>
</ul>

