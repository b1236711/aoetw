<h2>歷史(來源取自維基百科)</h2>
<div class="hrn1"></div>
<div class="n1">
 <ul>
  <li>羅馬帝國時期，匈牙利是潘諾尼亞行省，羅馬帝國滅亡後，
  各民族陸續遷移到這裡。首先到來的是匈人，在阿提拉的領導下，他們建立了強大的匈人帝國。匈牙利這個名字可能來源於此，</li>
  <li>但也有很多學者認為應該來自屬於突厥的歐諾古爾（Onogur）人。</li>
  <li>在匈人帝國解體後日耳曼部落統治了這裡將近100年，接踵而來的是阿瓦爾人，在他們近200年的統治下，
  斯拉夫民族開始滲入。摩拉維亞人、保加爾人、波蘭人和克羅埃西亞人都曾經企圖推翻阿瓦爾人，但直到查理曼才成功的將其擊敗。</li>
  <li>查理曼死後，東法蘭克王國逐步衰落，形勢對斯拉夫人的崛起有利，
  大摩拉維亞領袖斯瓦托普魯克雄心勃勃地企圖建立大摩拉維亞公國，但是東方遊牧民族馬扎爾人的到來結束了這一地區的紛爭。</li>
  
<li>阿爾帕德王朝:當時馬扎爾人遊牧部落從烏拉山西麓和伏爾加河灣一帶向西遷徙，公元896年在多瑙河盆地潘諾尼亞平原定居下來。
傳說當時7個馬札爾人部落與3個可薩人部落組合成一個聯盟，之後匈牙利王國逐漸成形。
第一個朝代是阿爾帕德大公所建立的阿爾帕德王朝。1000年，匈牙利大公伊什特萬一世在匈牙利推行天主教，並獲天主教教宗加冕成為匈牙利第一位國王。</li>
  </ul>

