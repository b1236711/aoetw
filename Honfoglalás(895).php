<h2>攻略</h2>
<div class="hrn1"></div>
<div class="n1">
 <ul>
  <li>第一階段</li>
  <li>1.  一開始提示說要你盡量保留部隊與牲畜，但實際上需要保留的牲畜只有馬比較重要。所以帶上你的馬以及騎乘部隊南下找隊友，至於牛跟羊，能帶多少就帶多少吧！</li>
  <li>2.  抵達卡札爾汗國之後，卡札爾汗國會給你一個城鎮中心在城外。雖然提示叫你防守它，但是根本守不住。大約佩切涅格人四五波襲擊之後會來一波超大的，然後提示會跟你說「我們守不住了，請帶20人逃到指定位置」。但是等到他說這句話，你的人根本已經走不掉了，所以一開始不斷生村民，在靠近你盟友的那個地方採石礦跟木頭就好，並且在周圍蓋幾個哨戰探查敵人動向，敵人來了就躲進盟友的城牆。他們看不到人就會去打城鎮中心。</li>
  <li>3.  累積足夠的木頭，等到提示說要逃亡的時候，把你的村民全都拉近橘色，從卡札爾汗國南邊城門出去，蓋個伐木場繼續把附近幾棵樹砍掉，然後蓋個磨坊去東方海邊捕魚，生矛兵或長槍兵補足20人，全體拉到旗幟的位置就可以達成任務。</li>
  <li>第二階段</li>
  <li>1.  你獲得許多城堡、村民、城鎮中心，而且附近有很多資源。提示又叫你防守它。不管怎樣守就對了。多蓋一些城堡並且研發彈道學與城垛射孔。並且一直升馬弓與驃騎兵。看到衝車就用驃騎兵去戳。</li>
   <li>2. 然而佩切涅格人很快會進入帝王時代，然後你又打不贏人家了。在你快要守不住的時候，拜占庭人會要求你幫它滅保加爾人，此時佩切涅格人對你的外交會暫時改成同盟。（然而他們的部隊會繼續集結到你的城堡附近，怕……）由於佩切涅格人暫時不會攻擊你，此時你可以大搖大擺的讓村民全地圖採礦，還可以派貿易車隊到北方佩切涅格人的基地去貿易。</li>
  <li>3.  繼續爆你的經濟，利用拜占庭人送你的運輸艦運送村民南下，在拜占庭人的城堡旁邊再蓋三座城堡，駐滿馬騎弓兵（別忘了給拜占庭人的城堡也駐滿）。然後把拜占庭人被撞掉的城門用自己的城門補上。就可以準備開始對付紫色。</li>
  <li>4.  保加爾人擁有幾乎無限多的資源，只要你的部隊殺他一隻村民，他就會開始瘋狂地用部隊攻擊你，但由於對方不會出攻城器，在四座城堡的掃射下防守不是問題。但是在矛兵的騷擾下，城堡HP還是會慢慢下降，所以要記得留兩隻村民等著修。</li>
  <li>5.  即使你拆掉保加爾人的城堡，或任何建築，他們也會補蓋，研究了很久，筆者發現唯一可以停止他們無限出兵的方式是摧毀城鎮中心，因為只要摧毀城鎮中心，他們出兵的頻率就會大幅減少，也就可以慢慢推平他們。因為你全地圖採礦還有大量貿易車隊，金礦不虞匱乏，所以可以很奢侈的生遊俠海去拆城鎮中心。</li>
  <li>6.  注意不要將村民趕盡殺絕，留越多越好，晚點還有用。</li> 
  <li>7.  當你擊敗保加爾人之後，佩切涅格人會立刻翻臉，然後你的城鎮又守不住了。提示會叫你帶60人前往旗幟處，湊滿人數達陣之後就可以過關。</li>
  <li>第三階段</li>
  <li>1.  達陣之後你除了僧侶之外的所有單位會被沒收，然後你的阿爾帕德跟一隻馬和信使會出現在地圖偏左的位置。把你的馬跟信使拿去給摩拉維亞人，再回到阿爾帕德身邊就可以獲得這片草原。（耶！又有家了！）然後任務提示就會變成叫你擊敗摩拉維亞人（What？人家給了你棲身之所你卻要擊敗人家？這裡筆者表示不能接受，馬扎爾人的理由是：我們用最好的馬換來這片草原，所以這片草原現在所有的地方都是我們的啦！）</li>
  <li>2.  推掉你營地西方的一座城堡就可以獲得整座城的控制權，包含兩座碼頭，小心你的碼頭是不能重建的，而其中一個碼頭因為距離太近，敵人的重投有可能會隔岸把它摧毀，要小心防守。</li>
  <li>3.  現在你可以造船了。由於你不能生村民，目前給的村民又很少，所以首先生產一些僧侶，造一艘運輸艦，把僧侶送到之前保加爾人的島上，再把對保加爾人的外交改中立，就可以招降之前你沒殺光的村民，可以大幅增加你的生產力。</li>
  <li>4.  在保加爾人的北側岸邊蓋兩座城堡，要同時完工同時駐滿馬騎弓兵，這樣就會吸引敵人的重投一直來南側送。（要蓋兩座，否則城堡會慢慢被大量的重投推掉）然後造大量戰船清掉摩拉維亞北方沿岸建築。</li>
  <li>5.  摩拉維亞沒有巨投，而且AI很奇怪，不會拆木牆，所以派運輸艦載村民從北岸登陸，蓋一圈木牆，再讓巨投登陸到木牆裡面，就可以輕鬆拆掉更裡面的建築。此時綠色沿岸已經陷落，派村民蓋十個馬廄就可以用肉馬淹死他們來結束遊戲。</li>
</ul>
