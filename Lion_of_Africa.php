<h2>斥侯回報</h2>
<div class="hrn1"></div>
<div class="n1">
<ul>
  <li>法蘭西斯科．德．阿爾梅達控制著盧克斯河中的一座島上的格拉西奧薩堡（藍）。
  這座島的資源十分有限，需要依靠河兩岸的柏柏城鎮來提供糧草。</li>

  <li>當地的埃米爾人（紅）統治著拉萊什城，守衛著格拉西奧薩堡以西的河口。
  他們訓練劍兵，戰矛兵和騎士，還有一隻強大的海軍。
  在我們葡萄牙槍砲的攻擊距離內他們對我們葡萄牙只得表現得十分友好。
  在我們擊退巴巴里海盜（綠）之前，他們應該都是一個值得爭取的異教徒盟軍。</li>

  <li>巴巴里海盜（綠）佔據了一個舊商隊旅館和河北岸的港口。
  只要他們的艦隊繼續壯大，能肆意截取基督徒和穆斯林的商旅貨物，他們就會是一個難啃的骨頭。
  海盜們不僅是久經風雨的水手，也是富有經驗的弓箭手和重裝長槍兵。</li>

  <li>巴努阿魯斯（黃）的輕騎兵和駱駝騎兵遍佈沙漠和東南邊境。
  他們是一支古老的族群，儘管伊斯蘭法典在當地延續了七百多年的影響，
  他們仍然像他們的怒米迪亞祖先在漢尼拔時代一樣，供奉有圖騰的女人。
  潛入沙漠，摧毀他們的營帳是終結他們入侵的唯一方法。</li>

  <li>在茅利塔尼亞海岸外的是加那利群島，卡斯提爾帝國的領土。
  歐洲條例在這個危險的前線是毫無效力的，然而這片島嶼可以提供豐富的資源，
  只要我們能招安當地的關契斯人（紫）。
  異教徒是柏柏人的親戚，他們時代都有將屍體做成木乃伊的傳統。
  他們披著山羊皮，帶著由骨頭和貝殼做成的項鍊，他們是久經沙場的戰矛兵和船夫。</li>

  <li>據說有人看到奧斯曼私掠船長凱瑪律‧列伊斯的旗艦（灰）為了幫格蘭納達的埃米爾解圍，
  在海上攻擊基督艦隊。打敗他們是我們未來征服摩爾人和奧斯曼人的必經之路。</li>

  <li>在非洲海岸的不遠處，有傳言說杜阿爾特帕切科佩雷拉率領的遠征軍神秘消失了...</li>
</ul>

<h2>玩家攻略</h2>
<div class="hrn1"></div>
<div class="n1">
<ol>
  <li>建議攻打順序：巴努阿魯斯（黃）→巴巴里海盜（綠）→轉為敵對的埃米爾（紅）。</li>
  <li>關卡初始，巴巴里海盜（綠）會馬上派運輸船運兵至島上，善用英雄及島上兵力解圍。</li>
  <li>大陸有多處我方單位建築，初期皆會被黃色和綠色騷擾，海面也有綠色船隻騷擾。</li>
  <li>無須發展海上勢力，我方島上顧好箭塔及城堡即可防禦敵方戰船（建議研發學院的預熱射擊及兵工廠相關範圍科技），另英雄單位無存活任務需求。</li>
  <li>大陸上我方馬廄先生產一名斥候，往地圖左下方山壁內探索觸發大地之母單位（消失的遠征軍）。</li>
  <li>因柏柏生產快速，須儘速將收納的遠征軍兵力優先摧毀散佈在地圖右半邊的巴努阿魯斯（黃）的5個營帳。</li>
  <li>營帳位置：遠征軍的山壁正上方1個→山壁右上方1個→往右直線走到底1個→貼著地圖邊邊往上走到底會碰到2個</li>
  <li>黃色和綠色兵力可用僧侶招降，尤其黃色很好招。初期遠征軍會有一名僧侶，可於攻打黃色營帳期間加減招降增添我方單位。</li>
  <li>遠征軍的聖物推車，可使用運輸傳送回島上，並放置城堡旁（功效不明）。</li>
  <li>拆除完黃色營帳後，兵力往大陸上方集結，準備攻打巴巴里海盜（綠）。</li>
  <li>此時建議在大陸上蓋城鎮中心，研發兵工廠科技、以及軍營縱火科技等，若有資源就升帝王時代。</li>
  <li>巴巴里海盜（綠）外圍的防禦箭塔可用近戰單位拆除，接著兩處狹窄入口可蓋城門擋住綠色兵力出入。</li>
  <li>去除綠色外圍的防禦箭塔後，其勢力右上方有少許石礦和金礦可採集。</li>  
  <li>帝王時代後研發化學即可生產火炮，能輕鬆破壞綠色城門、軍事建築、城牆內的防禦箭塔及城堡，另我方島上也可蓋火炮塔防禦。</li>
  <li>可不理會次要任務埃米爾（紅）的需求，因消滅巴巴里海盜（綠）後，埃米爾（紅）會由同盟轉為敵對。</li>
  <li>消滅綠色軍事建築時保留一個碼頭，派村民於埃米爾（紅）城門外蓋城門擋住，及伐掉門外樹木直接蓋城堡及其他軍事建築集結兵力，準備完畢後再打掉綠色碼頭。</li> 
  <li>凱馬律．列伊斯（灰）的船隻在左外海中間一帶，關契斯人（紫色）位於地圖左方的小島上。這兩個勢力無須特地攻打，因只要毀掉埃米爾（紅）的城堡即可獲得全面勝利。</li>
</ol>
</div>

