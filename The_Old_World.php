<h2>斥侯回報</h2>
<div class="hrn1"></div>
<div class="n1">
<ul>
  <li>法蘭西斯科‧德‧阿爾梅達控制著托洛（綠）的儲備物資，托洛是在杜羅河邊的城市。</li>

  <li>阿方索國王的軍隊（紅）被阿拉貢（黃）打敗後又上路了，殘餘力量潛伏在戰場的邊緣，只有胡安王子的出現才能重振士氣。軍隊由輕騎兵、長槍兵、弩兵和戟兵組成。</li>

  <li>阿拉貢（黃）有三個軍營控制這片區域。傳統軍隊包括騎兵、劍兵和弓箭手。
  阿拉貢（黃）虜獲了一支新的葡萄牙風琴砲。此外，阿爾巴公爵和門多薩紅衣主教會指揮軍隊於地圖中巡邏。</li>

  <li>薩莫拉城在托洛城的西邊，橫跨杜羅河。
  此城由與我們友好的胡安勢力（紫）和與我們敵對的伊莎貝拉勢力（灰）分而治之。
  前者控制著北岸和老羅馬教堂，後者控制著聖塔瑪爾塔的佩尼亞斯，一個佈滿岩石的能俯瞰河面的橋的空地。
  雙方都有軍隊：長槍兵、僱傭兵、弩兵、攻城武器和少數輕騎兵。</li>
</ul>
