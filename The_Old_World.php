<h2>次要任務</h2>
<div class="hrn1"></div>
<div class="n1">
  <ul>
    <li>讓胡安王子與剩餘的阿方索國王軍隊會合。（須由胡安王子抵達紅色軍隊處才可徵招）</li>
    <li>找出阿爾巴公爵和門多薩紅衣主教並將他們拿下。</li>
  </ul>
<br>
<h2>任務提示</h2>
<div class="hrn1"></div>
<div class="n1">
<ol>
  <li>你被限制在城堡時代，人口上限是100。</li>
  <li>阿拉貢（黃）可以不斷生產新單位，但是這階段你不可以，所以迅速找出阿方索國王軍隊（紅）極為關鍵。</li>
  <li>胡安王子是個很好的坦克，雖然任務並無要求該角色不能死亡，但他是觸發阿方索國王軍隊（紅）轉為我方單位的是必要人物。</li>
  <li>還沒找到僧侶單位時，可將軍隊退回托洛城（綠）尋求綠色僧侶回血幫助。</li>
  <li>阿拉貢（黃）有機率攻打托洛城（綠），若其城鎮中心被破壞，遊戲將以失敗告終。</li>
  <li>殺死阿爾巴公爵和門多薩紅衣主教可以讓你在開始階段得到一些資源。（他們在地圖巡邏的黃色單位之中）</li>
  <li>地圖最北端的阿方索國王軍隊（紅）有一輛中型衝車，中間可能會遇上黃色敵軍，建議沿著地圖邊緣行走去取得。</li>
  <li>阿拉貢（黃）軍營都有不少兵力及箭塔、甚至投石車，建議由胡安王子打先鋒，優先打掉攻城武器並吸引敵軍，再派衝車去撞箭塔。</li>
  <li>摧毀阿拉貢（黃）三個軍營後，伊莎貝拉勢力（灰）會由同盟轉為敵對，通往胡安勢力的斷橋會修復可通行，其建築物和城內兵力將轉為我方所擁有。</li>
  <li>通往伊莎貝拉勢力（灰）佔領的薩莫拉城有一座橋，橋下方有座城堡會造成玩家不易通行，可將從紅色徵招到的三台中型衝車先撞掉城堡，搭配原有兵力+後期生產的即可順利摧毀灰色。</li>
</ol>
</div>
